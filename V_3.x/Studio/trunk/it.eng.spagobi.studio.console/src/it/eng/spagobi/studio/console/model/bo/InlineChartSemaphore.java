package it.eng.spagobi.studio.console.model.bo;

public class InlineChartSemaphore extends InlineChart {
	private String column;
	private String thresholdFirstInt;
	private String thresholdSecondInt;
	private String thresholdType;
	private String tooltipGreen;
	private String tooltipYellow;
	private String tooltipRed;
	/**
	 * @return the column
	 */
	public String getColumn() {
		return column;
	}
	/**
	 * @param column the column to set
	 */
	public void setColumn(String column) {
		this.column = column;
	}
	/**
	 * @return the thresholdFirstInt
	 */
	public String getThresholdFirstInt() {
		return thresholdFirstInt;
	}
	/**
	 * @param thresholdFirstInt the thresholdFirstInt to set
	 */
	public void setThresholdFirstInt(String thresholdFirstInt) {
		this.thresholdFirstInt = thresholdFirstInt;
	}
	/**
	 * @return the thresholdSecondInt
	 */
	public String getThresholdSecondInt() {
		return thresholdSecondInt;
	}
	/**
	 * @param thresholdSecondInt the thresholdSecondInt to set
	 */
	public void setThresholdSecondInt(String thresholdSecondInt) {
		this.thresholdSecondInt = thresholdSecondInt;
	}
	/**
	 * @return the thresholdType
	 */
	public String getThresholdType() {
		return thresholdType;
	}
	/**
	 * @param thresholdType the thresholdType to set
	 */
	public void setThresholdType(String thresholdType) {
		this.thresholdType = thresholdType;
	}
	/**
	 * @return the tooltipGreen
	 */
	public String getTooltipGreen() {
		return tooltipGreen;
	}
	/**
	 * @param tooltipGreen the tooltipGreen to set
	 */
	public void setTooltipGreen(String tooltipGreen) {
		this.tooltipGreen = tooltipGreen;
	}
	/**
	 * @return the tooltipYellow
	 */
	public String getTooltipYellow() {
		return tooltipYellow;
	}
	/**
	 * @param tooltipYellow the tooltipYellow to set
	 */
	public void setTooltipYellow(String tooltipYellow) {
		this.tooltipYellow = tooltipYellow;
	}
	/**
	 * @return the tooltipRed
	 */
	public String getTooltipRed() {
		return tooltipRed;
	}
	/**
	 * @param tooltipRed the tooltipRed to set
	 */
	public void setTooltipRed(String tooltipRed) {
		this.tooltipRed = tooltipRed;
	}
	
}
