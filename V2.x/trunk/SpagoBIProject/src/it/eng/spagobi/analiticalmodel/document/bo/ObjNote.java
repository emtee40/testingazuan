package it.eng.spagobi.analiticalmodel.document.bo;

import java.io.Serializable;

public class ObjNote implements Serializable {

	Integer id = null;
	Integer biobjId = null;
	Integer binId = null;
	String execReq = null;
    byte[] content = null;
    
    
	/**
	 * Gets the exec req.
	 * 
	 * @return the exec req
	 */
	public String getExecReq() {
		return execReq;
	}
	
	/**
	 * Sets the exec req.
	 * 
	 * @param execReq the new exec req
	 */
	public void setExecReq(String execReq) {
		this.execReq = execReq;
	}
	
	/**
	 * Gets the id.
	 * 
	 * @return the id
	 */
	public Integer getId() {
		return id;
	}
	
	/**
	 * Sets the id.
	 * 
	 * @param id the new id
	 */
	public void setId(Integer id) {
		this.id = id;
	}
	
	/**
	 * Gets the biobj id.
	 * 
	 * @return the biobj id
	 */
	public Integer getBiobjId() {
		return biobjId;
	}
	
	/**
	 * Sets the biobj id.
	 * 
	 * @param biobjId the new biobj id
	 */
	public void setBiobjId(Integer biobjId) {
		this.biobjId = biobjId;
	}
	
	/**
	 * Gets the bin id.
	 * 
	 * @return the bin id
	 */
	public Integer getBinId() {
		return binId;
	}
	
	/**
	 * Sets the bin id.
	 * 
	 * @param binId the new bin id
	 */
	public void setBinId(Integer binId) {
		this.binId = binId;
	}
	
	/**
	 * Gets the content.
	 * 
	 * @return the content
	 */
	public byte[] getContent() {
		return content;
	}
	
	/**
	 * Sets the content.
	 * 
	 * @param content the new content
	 */
	public void setContent(byte[] content) {
		this.content = content;
	}
	
	
}
